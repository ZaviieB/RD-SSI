function [t ym w F1 f1]=HWF(y,dt)
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%       y     ==> 輸出檔案              
%       dt    ==> 取樣間隔        
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

[N l]=size(y);
if (N<l);
    y=y';
    [N l]=size(y);
end
t=0:dt:(N-1)*dt; %%% 開始:間距:結束 
w=0:1/((N-1)*dt):1/dt; 
for k=1:l
    ym(:,k)=y(:,k)-mean(y(:,k)); %%% Zero mean
    f1(:,k)=fft(ym(:,k))*dt;
    F1(:,k)=abs(f1(:,k));
end
    
   